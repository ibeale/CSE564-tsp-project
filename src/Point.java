

public class Point {
	private float x;
	private float y;
	private int index;
	
	public Point(float x, float y, int index) {
		this.x=x;
		this.y=y;
		this.index=index;
	}

	public float getX() {
		return x;
	}

	public float getY() {
		return y;
	}

	public int getIndex() {
		return index;
	}
	
	
}
